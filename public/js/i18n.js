// Apply translations to DOM
function applyLanguage(lang) {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.dataset.i18n;
    if (translations[lang]?.[key]) el.textContent = translations[lang][key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (translations[lang]?.[key]) el.placeholder = translations[lang][key];
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach(el => {
    const key = el.dataset.i18nAriaLabel;
    if (translations[lang]?.[key]) el.setAttribute("aria-label", translations[lang][key]);
  });

  document.documentElement.lang = lang;
}

// Initialize i18n and listen for language changes
export function initI18n() {
  let currentLang = localStorage.getItem("lang") || "en";
  applyLanguage(currentLang);

  window.addEventListener("languageChanged", e => {
    currentLang = e.detail;
    localStorage.setItem("lang", currentLang);
    applyLanguage(currentLang);
  });
}

// Re-apply language on back/forward navigation
window.addEventListener("pageshow", () => {
  const lang = localStorage.getItem("lang") || "en";
  applyLanguage(lang);
});

// Translation helper
export function t(key) {
  const lang = localStorage.getItem("lang") || "en";
  return translations[lang]?.[key] ?? key;
}

export function tPlural(key, count) {
  const lang = localStorage.getItem("lang") || "en";

  let suffix = count === 1 ? "_singular" : "_plural";
  const translationKey = key + suffix;

  return translations[lang]?.[translationKey] ?? key;
}
